use x11rb::protocol::xproto::Rectangle;

pub fn text_width(text: &str, scale: u16) -> u16 {
    let count = text.chars().count().min(u16::MAX as usize) as u16;
    count.saturating_mul(6).saturating_mul(scale)
}

pub fn rectangles(text: &str, x: i16, y: i16, scale: u16) -> Vec<Rectangle> {
    let mut output = Vec::new();
    let mut cursor_x = i32::from(x);
    let scale_i32 = i32::from(scale);
    for character in text.chars() {
        let rows = glyph(character.to_ascii_uppercase());
        for (row_index, row) in rows.into_iter().enumerate() {
            for column in 0..5_u8 {
                if row & (1 << (4 - column)) == 0 {
                    continue;
                }
                let px = cursor_x + i32::from(column) * scale_i32;
                let py = i32::from(y) + row_index as i32 * scale_i32;
                if let (Ok(px), Ok(py)) = (i16::try_from(px), i16::try_from(py)) {
                    output.push(Rectangle {
                        x: px,
                        y: py,
                        width: scale,
                        height: scale,
                    });
                }
            }
        }
        cursor_x += 6 * scale_i32;
    }
    output
}

